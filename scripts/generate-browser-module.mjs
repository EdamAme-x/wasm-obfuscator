#!/usr/bin/env node
import fs from "node:fs";
import os from "node:os";
import path from "node:path";
import { spawnSync } from "node:child_process";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, "..");

function resolveMoonCommand() {
  if (process.env.MOON && process.env.MOON.trim().length > 0) {
    return process.env.MOON;
  }

  const defaultMoon = path.join(os.homedir(), ".moon", "bin", "moon");
  if (fs.existsSync(defaultMoon)) {
    return defaultMoon;
  }
  return "moon";
}

function runMoonBuildIfNeeded() {
  if (process.env.SKIP_MOON_BUILD === "1") {
    return;
  }

  const moonCmd = resolveMoonCommand();
  const build = spawnSync(moonCmd, ["build", "cmd/main", "--target", "js", "--release"], {
    cwd: repoRoot,
    encoding: "utf8",
  });
  if (build.status !== 0) {
    const stderr = (build.stderr || "").trim();
    const stdout = (build.stdout || "").trim();
    throw new Error(`moon build failed: ${stderr || stdout || "unknown error"}`);
  }
}

function extractSymbol(source, pattern, label) {
  const match = source.match(pattern);
  if (!match) {
    throw new Error(`failed to locate symbol for ${label}`);
  }
  return match[1];
}

function stripCliIife(source) {
  const marker = "\n(() => {";
  const index = source.lastIndexOf(marker);
  if (index === -1) {
    throw new Error("failed to locate CLI entry IIFE in generated JS");
  }
  return source.slice(0, index).trimEnd() + "\n";
}

function resolveOutputPaths(args) {
  if (args.length === 0) {
    throw new Error(
      "usage: node scripts/generate-browser-module.mjs <output-path> [<output-path>...]",
    );
  }
  return args.map((target) => (path.isAbsolute(target) ? target : path.resolve(repoRoot, target)));
}

function generateModuleSource(coreSource, symbols) {
  return `/* Auto-generated by scripts/generate-browser-module.mjs. DO NOT EDIT. */
${coreSource}
const __moonbitObfuscate = ${symbols.obfuscate};
const __moonbitObfuscateWat = ${symbols.obfuscateWat};
const __moonbitParseWatToWasm = ${symbols.parseWatToWasm};
const __moonbitDefaultConfig = ${symbols.defaultConfig};

const __WASM_HEADER = new Uint8Array([0x00, 0x61, 0x73, 0x6d, 0x01, 0x00, 0x00, 0x00]);
const __PASS_ID_TO_NAME = [
  "RenameIdentifiers",
  "SplitBlocks",
  "FlattenControlFlow",
  "EncodeConstants",
  "InsertOpaquePredicates",
];
const __PASS_NAME_TO_ID = {
  RenameIdentifiers: 0,
  SplitBlocks: 1,
  FlattenControlFlow: 2,
  EncodeConstants: 3,
  InsertOpaquePredicates: 4,
};

function __errorMessage(error) {
  if (typeof error === "string") {
    return error;
  }
  if (error instanceof Error && typeof error.message === "string") {
    return error.message;
  }
  if (error && typeof error._0 === "string") {
    return error._0;
  }
  if (error && typeof error.message === "string") {
    return error.message;
  }
  try {
    return JSON.stringify(error);
  } catch (_e) {
    return String(error ?? "Unknown error");
  }
}

function __unwrapResult(result, action) {
  if (result && result.$tag === 1) {
    return result._0;
  }
  const innerError = result && Object.prototype.hasOwnProperty.call(result, "_0") ? result._0 : result;
  throw new Error(action + " failed: " + __errorMessage(innerError));
}

function __toUint8Array(inputBytes) {
  if (inputBytes instanceof Uint8Array) {
    return new Uint8Array(inputBytes);
  }
  if (inputBytes instanceof ArrayBuffer) {
    return new Uint8Array(inputBytes);
  }
  if (ArrayBuffer.isView(inputBytes)) {
    return new Uint8Array(inputBytes.buffer, inputBytes.byteOffset, inputBytes.byteLength);
  }
  throw new Error("inputBytes must be a Uint8Array, ArrayBuffer, or ArrayBuffer view");
}

function __hasWasmHeader(inputBytes) {
  if (inputBytes.length < __WASM_HEADER.length) {
    return false;
  }
  for (let i = 0; i < __WASM_HEADER.length; i += 1) {
    if (inputBytes[i] !== __WASM_HEADER[i]) {
      return false;
    }
  }
  return true;
}

function __detectInputFormatFromBytes(inputBytes) {
  if (__hasWasmHeader(inputBytes)) {
    return "wasm";
  }
  const sample = new TextDecoder().decode(inputBytes.subarray(0, 1024));
  if (sample.includes("\u0000")) {
    return "wasm";
  }
  const trimmed = sample.trimStart();
  if (trimmed.startsWith("(module") || trimmed.startsWith("(") || trimmed.startsWith(";;")) {
    return "wat";
  }
  return "wasm";
}

function __readNumber(candidate, fallback, optionName) {
  if (candidate === undefined || candidate === null || candidate === "") {
    return fallback;
  }
  const parsed = Number(candidate);
  if (!Number.isInteger(parsed)) {
    throw new Error(optionName + " must be an integer");
  }
  return parsed;
}

function __normalizePass(pass) {
  if (typeof pass === "number" && Number.isInteger(pass) && pass >= 0 && pass <= 4) {
    return pass;
  }
  if (typeof pass === "string" && Object.prototype.hasOwnProperty.call(__PASS_NAME_TO_ID, pass)) {
    return __PASS_NAME_TO_ID[pass];
  }
  throw new Error("Unsupported pass value: " + String(pass));
}

function __buildConfig(options = {}) {
  const defaults = __moonbitDefaultConfig();
  const passes = Array.isArray(options.passes)
    ? options.passes.map((pass) => __normalizePass(pass))
    : defaults.passes.slice();

  return {
    passes,
    random_seed: __readNumber(
      options.seed ?? options.randomSeed ?? options.random_seed,
      defaults.random_seed,
      "seed",
    ),
    max_size_growth_percent: __readNumber(
      options.maxSizeGrowthPercent ?? options.max_size_growth_percent,
      defaults.max_size_growth_percent,
      "maxSizeGrowthPercent",
    ),
    max_runtime_slowdown_percent: __readNumber(
      options.maxRuntimeSlowdownPercent ?? options.max_runtime_slowdown_percent,
      defaults.max_runtime_slowdown_percent,
      "maxRuntimeSlowdownPercent",
    ),
    emit_spy_trace: Boolean(
      options.spy ?? options.emitSpyTrace ?? options.emit_spy_trace ?? defaults.emit_spy_trace,
    ),
  };
}

function __normalizeSpyEvents(spyEvents) {
  return Array.isArray(spyEvents)
    ? spyEvents.map((event) => ({
        pass: event.pass,
        passName: __PASS_ID_TO_NAME[event.pass] ?? String(event.pass),
        phase: event.phase,
        detail: event.detail,
      }))
    : [];
}

function __normalizeResult(rawResult) {
  const outputBytes = new Uint8Array(rawResult.obfuscated_wasm);
  const metrics = {
    inputSize: rawResult.metrics.input_size,
    outputSize: rawResult.metrics.output_size,
    sizeGrowthPercent: rawResult.metrics.size_growth_percent,
    estimatedRuntimeSlowdownPercent: rawResult.metrics.estimated_runtime_slowdown_percent,
    input_size: rawResult.metrics.input_size,
    output_size: rawResult.metrics.output_size,
    size_growth_percent: rawResult.metrics.size_growth_percent,
    estimated_runtime_slowdown_percent: rawResult.metrics.estimated_runtime_slowdown_percent,
  };
  const spyEvents = __normalizeSpyEvents(rawResult.spy_events);
  return {
    outputBytes,
    metrics,
    spyEvents,
    spy_events: spyEvents,
  };
}

export function defaultConfig() {
  const defaults = __moonbitDefaultConfig();
  return {
    passes: defaults.passes.map((pass) => __PASS_ID_TO_NAME[pass] ?? String(pass)),
    randomSeed: defaults.random_seed,
    maxSizeGrowthPercent: defaults.max_size_growth_percent,
    maxRuntimeSlowdownPercent: defaults.max_runtime_slowdown_percent,
    emitSpyTrace: defaults.emit_spy_trace,
    random_seed: defaults.random_seed,
    max_size_growth_percent: defaults.max_size_growth_percent,
    max_runtime_slowdown_percent: defaults.max_runtime_slowdown_percent,
    emit_spy_trace: defaults.emit_spy_trace,
  };
}

export function parseWatToWasm(inputWat) {
  if (typeof inputWat !== "string") {
    throw new Error("inputWat must be a string");
  }
  const wasm = __unwrapResult(__moonbitParseWatToWasm(inputWat), "parseWatToWasm");
  return new Uint8Array(wasm);
}

export function obfuscateWat(inputWat, options = {}) {
  if (typeof inputWat !== "string") {
    throw new Error("inputWat must be a string");
  }
  const config = __buildConfig(options);
  const rawResult = __unwrapResult(__moonbitObfuscateWat(inputWat, config), "obfuscateWat");
  return {
    inputFormat: "wat",
    ...__normalizeResult(rawResult),
  };
}

export function obfuscateBytes(options) {
  if (!options || options.inputBytes === undefined || options.inputBytes === null) {
    throw new Error("inputBytes is required");
  }

  const requestedInputFormat = options.inputFormat ?? options.format ?? "auto";
  if (requestedInputFormat !== "wasm" && requestedInputFormat !== "wat" && requestedInputFormat !== "auto") {
    throw new Error("Unsupported inputFormat: " + String(requestedInputFormat));
  }

  const inputBytes = __toUint8Array(options.inputBytes);
  const inputFormat = requestedInputFormat === "auto"
    ? __detectInputFormatFromBytes(inputBytes)
    : requestedInputFormat;

  const config = __buildConfig(options);
  const rawResult = inputFormat === "wat"
    ? __unwrapResult(__moonbitObfuscateWat(new TextDecoder().decode(inputBytes), config), "obfuscateBytes")
    : __unwrapResult(__moonbitObfuscate(inputBytes, config), "obfuscateBytes");

  return {
    inputFormat,
    ...__normalizeResult(rawResult),
  };
}
`;
}

function main() {
  const outputPaths = resolveOutputPaths(process.argv.slice(2));
  runMoonBuildIfNeeded();

  const generatedPath = path.join(repoRoot, "_build", "js", "release", "build", "cmd", "main", "main.js");
  const rawSource = fs.readFileSync(generatedPath, "utf8");
  const coreSource = stripCliIife(rawSource);

  const symbols = {
    obfuscate: extractSymbol(coreSource, /function\s+([A-Za-z0-9_$]*obfuscator9obfuscate)\s*\(/, "obfuscate"),
    obfuscateWat: extractSymbol(coreSource, /function\s+([A-Za-z0-9_$]*obfuscator14obfuscate__wat)\s*\(/, "obfuscate_wat"),
    parseWatToWasm: extractSymbol(coreSource, /function\s+([A-Za-z0-9_$]*obfuscator20parse__wat__to__wasm)\s*\(/, "parse_wat_to_wasm"),
    defaultConfig: extractSymbol(coreSource, /function\s+([A-Za-z0-9_$]*obfuscator15default__config)\s*\(/, "default_config"),
  };

  const output = generateModuleSource(coreSource, symbols);
  for (const target of outputPaths) {
    fs.mkdirSync(path.dirname(target), { recursive: true });
    fs.writeFileSync(target, output, "utf8");
  }

  const relativeTargets = outputPaths.map((target) => path.relative(repoRoot, target));
  console.log(`generated browser module: ${relativeTargets.join(", ")}`);
}

main();
